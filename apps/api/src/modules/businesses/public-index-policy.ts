/**
 * Sprint 29 — чи віддавати публічну сторінку пошуковим системам.
 *
 * Для звичайних отримувачів правило незмінне з Sprint 23: вирішує користувацький
 * тогл `seoIndexEnabled`. Прапорці каталогу на індексацію не впливають, бо
 * публічність за планом означає видимість у каталозі, а не доступність сторінок:
 * приховані реквізити звичайного бізнесу далі індексуються за прямим посиланням.
 *
 * Для системних (адмінських) отримувачів навпаки: `catalogVisible` це єдиний
 * важіль адміна, і він мусить діяти на пошук. Сценарій плану «держава змінила
 * рахунок: створюємо нові реквізити і ховаємо старі» інакше лишав би застарілі
 * державні реквізити в індексі нарівні з новими, а платіж за застарілим IBAN не
 * доходить. `seoIndexEnabled` у системних записів завжди `true`
 * (`createSystemPayee`) і в адмінському контракті його немає саме тому, що
 * приховання керується прапорцями каталогу.
 *
 * Наслідок для sitemap: та сама функція фільтрує URL, тож сторінка ніколи не
 * подається Google з `noindex` на ній (помилка в Search Console).
 */
/**
 * Sprint 29 — чи показувати реквізити у списку на публічній сторінці отримувача.
 *
 * Та сама асиметрія, що в `resolvePublicIndexEnabled`, і з тієї ж причини. Для
 * звичайного отримувача `catalogVisible` керує лише каталогом, а сторінка
 * лишається повним списком його реквізитів: дефолт прапорця `false`, тож фільтр
 * тут спорожнив би публічні сторінки всім наявним користувачам.
 *
 * Для системного (адмінського) отримувача навпаки: приховані реквізити мусять
 * зникати і зі сторінки. Інакше сценарій плану «держава змінила рахунок:
 * створюємо нові реквізити і ховаємо старі» лишав би застарілий державний IBAN
 * у списку поруч з новим — картка каталогу веде саме на цю сторінку, тож платник
 * з каталогу бачив би обидва без візуальної різниці і міг заплатити на мертвий
 * рахунок. Прямі посилання на сам рахунок при цьому працюють як і раніше
 * (інваріант «публічність це видимість у каталозі, не доступність сторінок»).
 */
export function isPublicAccountListed(
    business: { isSystem?: boolean },
    account: { catalogVisible?: boolean }
): boolean {
    if (!business.isSystem) return true;
    return account.catalogVisible === true;
}

export function resolvePublicIndexEnabled(
    business: {
        seoIndexEnabled: boolean;
        isSystem?: boolean;
        catalogVisible?: boolean;
    },
    account?: { catalogVisible?: boolean }
): boolean {
    if (!business.seoIndexEnabled) return false;
    if (!business.isSystem) return true;
    if (!business.catalogVisible) return false;
    return account === undefined || account.catalogVisible === true;
}
