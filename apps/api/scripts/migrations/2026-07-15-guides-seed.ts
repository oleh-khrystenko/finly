export interface MigrationResult {
    seeded: number;
    skipped: number;
}

export async function runMigration(db: Db): Promise<MigrationResult> {
    const collection = db.collection(GUIDES_COLLECTION);
    let seeded = 0;
    let skipped = 0;

    for (const guide of SEED_GUIDES) {
        const exists = await collection.findOne({ slug: guide.slug });
        if (exists) {
            skipped += 1;
            continue;
        }
        const now = new Date();
        await collection.insertOne({
            ...guide,
            status: 'draft',
            datePublished: null,
            dateModified: null,
            createdAt: now,
            updatedAt: now,
        });
        seeded += 1;
    }

    return { seeded, skipped };
}

/** CLI entry point — connect → run → disconnect → log (патерн deploy.yml). */
async function cli(): Promise<void> {
    const uri = process.env.MONGODB_URI;
    if (!uri) {
        console.error(
            '[migration:guides-seed] MONGODB_URI is required (export from .env or pass via docker-compose env_file)'
        );
        process.exit(1);
    }

    await mongoose.connect(uri);
    try {
        const db = mongoose.connection.db;
        if (!db) {
            throw new Error(
                '[migration:guides-seed] mongoose.connection.db is undefined after connect'
            );
        }
        const result = await runMigration(db);

        console.log('[migration:guides-seed] applied', result);
    } finally {
        await mongoose.disconnect();
    }
}

if (require.main === module) {
    cli().catch((err) => {
        console.error('[migration:guides-seed] failed', err);
        process.exit(1);
    });
}
